#include "../../inc/asm.h"

int		convert_int_endian(int num)
{
	unsigned int swapped;

	swapped = ((num >> 24) & 0xff) |
		((num << 8) & 0xff0000) |
		((num >> 8) & 0xff00) |
		((num << 24) & 0xff000000);
	return (swapped);
}


